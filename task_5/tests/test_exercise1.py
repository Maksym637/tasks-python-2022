from exercise1 import spiral

def test_spiral():
    assert spiral(3, 3) == [[1, 2, 3], [8, 9, 4], [7, 6, 5]]
    assert spiral(3, 4) == [[1, 2, 3, 4], [10, 11, 12, 5], [9, 8, 7, 6]]
    assert spiral(4, 3) == [[1, 2, 3], [10, 11, 4], [9, 12, 5], [8, 7, 6]]
    assert spiral(5, 5) == [[1, 2, 3, 4, 5], [16, 17, 18, 19, 6], [15, 24, 25, 20, 7], [14, 23, 22, 21, 8], [13, 12, 11, 10, 9]]